// NEW: Get random example sentence
export function getRandomExample(category = null) {
  if (category && EXAMPLE_SENTENCES[category]) {
    const examples = EXAMPLE_SENTENCES[category];
    return examples[Math.floor(Math.random() * examples.length)];
  }
  
  const allCategories = Object.keys(EXAMPLE_SENTENCES);
  const randomCategory = allCategories[Math.floor(Math.random() * allCategories.length)];
  const examples = EXAMPLE_SENTENCES[randomCategory];
  return examples[Math.floor(Math.random() * examples.length)];
}

